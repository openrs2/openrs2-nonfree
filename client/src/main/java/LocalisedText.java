	@OriginalMember(owner = "client!hk", name = "a", descriptor = "(IZ)V")
	public static void setLanguage(@OriginalArg(0) int language) {
		if (language == 0) {
			return;
		}
		if (language == 1) {
			switchToGerman();
		} else if (language == 2) {
			switchToFrench();
		} else if (language == 3) {
			switchToPortuguese();
		} else {
			throw new RuntimeException();
		}
	}
